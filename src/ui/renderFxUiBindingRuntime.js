      waterFlowSpeedValue: deps.waterFlowSpeedValue,
      waterFlowScaleValue: deps.waterFlowScaleValue,
      waterShimmerStrengthValue: deps.waterShimmerStrengthValue,
      waterGlintStrengthValue: deps.waterGlintStrengthValue,
