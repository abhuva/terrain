      clamp: deps.clamp,
      serializeWaterSettings: deps.serializeWaterSettings,
      waterFlowDirectionValue: deps.waterFlowDirectionValue,
      waterLocalFlowMixValue: deps.waterLocalFlowMixValue,
      waterDownhillBoostValue: deps.waterDownhillBoostValue,
